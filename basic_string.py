# we can index a string
s = "banana"
print(s[1]) # indexing start from zero
print(s[0]) # first letter
print(s[10//2])

# you can also index by negatives!
print(s[-1])
print(s[-6]) # same as s[0]

# len function will tell you the amount of characters
print(s, len(s)) 
